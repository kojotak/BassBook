package com.github.kojotak.bassbook.data;

public enum Tuning {

    /**
     * Standard tuning for 4 string
     */
    EADG,

    /**
     * Half step down tuning for 4 string
     */
    EbAbDbGb,

    /**
     * Drop D for 4 string
     */
    DADG,

    CGCF,

    /**
     * Drop C# for $ string, that is C#G#C#F#
     */
    CsGsCsFs,

    DGCF,

    /**
     * Standard tuning for 5 string
     */
    HEADG
}
